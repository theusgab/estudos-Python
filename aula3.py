# TIPOS DE DADOS

"""
str - string - textos 'Assim' ou "Assim"
int - inteiro - numeros positivos ou negativos que não possuem pontos 
float - real/ponto flutuante - numeros positivos ou negativos que possuem pontos
bool - boleano/lógico - True/False

"""

print('Luiz', type('Luiz')) #str
print(10, type(10)) #int
print(10.5, type(10.5)) #float
print(10 == 10, type(10 == 10)) #bool


