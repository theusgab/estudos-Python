# IF E COMPARAÇÃO

"""
- Faça um programa onde o usuario digite dois valores
- Na saida tera que dizer se os numeros digitados são maiores ou menores
"""

primeiro_valor = input('Digite um numero: ')
segundo_valor = input('Digite outro valor: ')

if primeiro_valor >= segundo_valor:
    print(f'{primeiro_valor} é maior ou igual ao {segundo_valor}')

else:
    print(f'{primeiro_valor} é menor que {segundo_valor}')
